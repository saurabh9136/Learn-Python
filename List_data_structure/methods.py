list = [3, 5, 1, 8, 6]

#add element in end
list.append(24)

#to sort in ascending order
list.sort()

#to sort in descending order
list.sort(reverse=True)

# to reverse list
list.reverse()

#add element at index
list.insert(2, 4)

#remove first occurance of element
list.remove(3)

#remove at element index
list.pop(2)

print(list)